        <p>
            Al igual que los hijos heredamos caracteristicas de nuestros padres, en la POO podemos obtener clases hijas
            con el mismo contenido de su calse padre. De esta manera se obtiene una reutilizacion de las clases, la cual 
            es una de las propiedades mas deseadas en la progrmacion ya que conlleva un ahorro de tiempo y lineas de codigo.
            <br><br>
            La Reutilizacion a sido abordada en la programacion estructural por medio de funciones. Esto permite crear
            lineas de codigo que pueden ser invocadas tantas veces como sea necesario. En la POO este concepto es extendido a las
            clases, de manera que no solo se utilizan clases existentes, sino que tambien se asicionen nuevas variables y metodos
            a estas sin modificar el contenido original. Esta reutilizacion se logra por medio de la herencia de clases, donde 
            la clase hija tiene a su disposicion todos los miembros de la clase padre, llamada tambien SUPERCLASE o CLASE 
            BASE. La clase hija es llamada SUBCLASE o CLASE HEREDADA.
            <br><br>

            Graficamente, en el diagrama de clases la herencia se representa con una flecha desde la subclase, que indica
            que la subclase hereda los metodos y atributos de la superclase. 
