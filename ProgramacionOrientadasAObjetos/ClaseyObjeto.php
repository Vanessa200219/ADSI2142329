        <h2>OBJETO</h2>

        <P>
            Es una instalacion de una clase, es decir la materializacion de clase. Cuando se instancia un Objeto  se asigna
            datos a las variables de la clase y se puede ejecutar los metodos. Facilmente se pueden diferenciar las clases 
            de los objetos con el siguiente paralelo: las clases son moldes de galletas y los objetos son las galletas creadas
            con los moldes. Todas las galletas creadas con el mismo molde son iguales, asi que todos los objetos instanciados 
            de la misma clase tiene las mismas variables y los mismos metodos disponibles. Se diferencia en los valores que se 
            asigna a las variables.
