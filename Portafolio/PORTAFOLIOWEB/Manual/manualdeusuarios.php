        <div class="model2">
            <p class="firtsteam">Requisitos de Conocimiento para manejar el sistema</p>
            <br>
            En este apartado se debe indificar si los usuarios que van a manejar el 
            sistema, requieren conocimientos técnicos particulares, como algunos de 
            los siguientes: <br>
            <li>Informática Basica</li>
            <li>Sistemas Opreativos</li>
            <li>Conocer las políticas de seguridad de la institución como por ejemplo, 
                las políticas de ingreso, validacíon de usuarios y manejo de contraseñas
            </li>
            <br>
            Esto también se puede estructurar por perfiles de usuario.
        </div>


        <div class="model2">
            <p class="firtsteam">Requerimientos de Hardware y Software</p> 
            <br>
            Es necesario indicar las especificaciones técnicas del hardware y software
            que se deben utilizar para que el sistema funcione adecuadamente, estas 
            aplicaciones ya fueron definidas en los documentos de diseño y validaciones 
            posteriormente durante el proceso de construccion de la aplicacion.
            <br>
            En los Requerimientos de Software, se debe aclarar si hay limitaciones en 
            cuanto al sistema operativo, el gestor de bases de datos y de mas 
            herramientas necesarias para el buen funcionamiento del sistema de 
            informacion.
            <br>
            Si nuestro cliente, maneja varias versiones de sistemas operativos se debe
            dejar estipulado lo necesario para realizar la instalación y configuración 
            sobre cada una de las versiones de sistemas Operativos.
        </div>

