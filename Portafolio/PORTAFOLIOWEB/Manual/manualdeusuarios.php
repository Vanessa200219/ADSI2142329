            <section class="section">
            <p class="secondteam">Ingreso al Sistema</p>
            Se debe explicar detalladamente la forma de ingresar al sistema de 
            información, para cada uno de los usuarios que se tenga contemplados, 
            teniendo en cuenta el manejo de las políticas de seguridad de la información.
            </section>


            
            <section class="section">
            <p class="secondteam">Interfaz Graficas</p>
            En este capítulo se representa todo lo referente a la parte visual del
            aplicativo, con aspectos como, el mapa de navegación, la descripción del 
            miso y la descripción paso a paso de cada uno de los formularios que se 
            manejan en el aplicativo.
            </section>


            <section class="section">
            <p class="secondteam">Mapa de navegación </p>
            Se debe presentar de manera muy detallada, el mapa de navegación con sus 
            respectivos botones y funciones.
            <br>
            Por regla general, existe una estandarización en los botones de la barra de 
            menú, es por ello que en este capítulo se deben relacionar cada uno de los 
            botones, incluyendo, su nombre, imagen y su función.
            <br>
            Se debe indicar de manera general cual será la filosofía de navegación del
            sistema,  el  uso  de  los  menús,  botones  y  las recomendaciones 
            correspondientes con el inicio y cierre de sesiones.
            </section>


            <section class="section">
            <p class="secondteam">Descripción</p>
